    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String productID =  productIDField.getText();
        String productName = productNameField.getText();
        String manufacturerName = manufacturerNameField.getText();
        String supplierName = supplierNameField.getText();
        String quantity = quantityField.getText();
        String price = priceField.getText();
        
        if(productID.isEmpty() || productName.isEmpty() || manufacturerName.isEmpty() || supplierName.isEmpty() || quantity.isEmpty() || price.isEmpty())
            JOptionPane.showMessageDialog(null, "Some fields are empty");
        else{
        if(!isNumber(quantity) || !isNumber(price))
            JOptionPane.showMessageDialog(null, "You should enter a numeric value in the field whose name is \"Quantity\" or \"Price\"");
        
        else if(employee.productExist(productID))
            JOptionPane.showMessageDialog(null, "The product with id = " + productID + " already exists");

        else{
        employee.addProduct(productID, productName, manufacturerName, supplierName, Integer.parseInt(quantity), Float.parseFloat(quantity));
 
        productIDField.setText("");
        productNameField.setText("");
        manufacturerNameField.setText("");
        supplierNameField.setText("");
        quantityField.setText("");
        priceField.setText("");
        
        JOptionPane.showMessageDialog(null, "The product with id = " + productID + " has been successfully added"); 
        }
        }
        
    }//GEN-LAST:event_addButtonActionPerformed
        
